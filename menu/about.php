<div>
    Baseball for Bucks is a type of Fantasy Baseball, where instead of drafting current players, you try to outdo historical teams.
    <br>
    We have compiled every single player that has ever played in the Majors since 1920, and you can arrange the lineup of any team, the way you think they'll play best.
    <br>
    After you have finalized the lineup, the computer simulates every game that that teams has played that year, against their actual opponents, and you find out if you're a better manager than theirs.
    <br>
    You can research any team and player on Google before playing, and you can also have the computer try to find the best lineup.
    <br>
    Good Luck !
</div>
